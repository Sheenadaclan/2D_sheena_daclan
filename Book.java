public class Book{
    private String title;
    private int pages;
    private boolean isFictional;

    public String getTitle(){
        return title;
    }

    public void setTitle (String title){
        this.title = title;
    }

    public int getPages(){
        return pages;
    }

    public void setPages (int pages){
        this.pages = pages;
    }

    public boolean getIsFictional(){
        return isFictional;
    }

    public void setIsFiction (boolean isFictional){
        this.isFiction = isFictional;
    }
}